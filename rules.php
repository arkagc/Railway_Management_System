							<p><font color="red" size="+1"><b>Rule #2: Carrying birds in train</b></font>
							<font size="+1">If you want to travel with your avian bird then you have to adhere to certain Indian railways rules.
							First of all, you cannot carry the bird along with you in the regular coach. 
							Your bird will be treated as freight and placed in the luggage van. 
							Chapter 10 of the Railways Commercial Manual, clearly states that: “the responsibility for the well being of the bird (in this case) lies with the owner.” 
							You have to fill up a form where the value of the bird, their number (in case you are carrying multiple birds), the number of cages and its dimensions should be clearly mentioned.</font></p>
