							<p><font color="red" size="+1"><b>Rule #3: Refund policy for lost tickets</b></font>
							<font size="+1">Suppose you have an upcoming journey, and find that somehow you have lost or misplaced the ticket/s, there is still a way to go ahead with the journey. 
							You can approach the Chief Reservation Supervisor at the boarding station and submit an application requesting the issuance of a duplicate ticket. Alongside the application you would be asked to submit a xerox copy of identity card.
							This request would be entertained only until 24 hours before the journey time. 
							After inspection, the duplicate ticket would be provided on payment of a nominal processing fee.</font>
						</p>
